"""Quick mel-L1 / MR-STFT scorer for the NVFP4 listen test.

Compares listen/nvfp4/*.wav against listen/bf16_60s/*.wav (both 60s clips
freshly generated by nvfp4_listen_test.py). Skips CLAP (noisy + slow);
mel-L1 and MR-STFT are the cleanest signals per INDEX.md.
"""
from __future__ import annotations
import os
import sys
from pathlib import Path

import numpy as np
import torch
import soundfile as sf

MEL_SR = 48000
N_FFT = 2048
N_MELS = 128
HOP = 512

# Same MR-STFT config as score_variants.py for direct comparability.
MRSTFT_FFT_SIZES = (512, 1024, 2048)
MRSTFT_HOP_SIZES = (50, 120, 240)
MRSTFT_WIN_SIZES = (240, 600, 1200)


def _load_wav_mono(path, sr):
    wav, file_sr = sf.read(str(path), dtype="float32", always_2d=True)
    wav = torch.from_numpy(wav.T)  # (C, T)
    if wav.shape[0] > 1:
        wav = wav.mean(dim=0, keepdim=True)
    wav = wav.squeeze(0)
    if file_sr != sr:
        # Simple linear interp resample - good enough for mel comparison
        import torch.nn.functional as F
        wav = wav.view(1, 1, -1)
        new_len = int(wav.shape[-1] * sr / file_sr)
        wav = F.interpolate(wav, size=new_len, mode="linear", align_corners=False)
        wav = wav.view(-1)
    return wav


def _mel_log(x, device):
    """log(1 + mel_spectrogram) using torch.stft + a mel filterbank."""
    # Build mel filterbank once per device.
    if not hasattr(_mel_log, "_fb_cache"):
        _mel_log._fb_cache = {}
    if device not in _mel_log._fb_cache:
        # Simple triangular mel filterbank.
        f_lo, f_hi = 0.0, MEL_SR / 2
        mel_lo = 2595 * np.log10(1 + f_lo / 700)
        mel_hi = 2595 * np.log10(1 + f_hi / 700)
        mels = np.linspace(mel_lo, mel_hi, N_MELS + 2)
        hz = 700 * (10 ** (mels / 2595) - 1)
        bins = np.floor((N_FFT + 1) * hz / MEL_SR).astype(int)
        fb = np.zeros((N_MELS, N_FFT // 2 + 1), dtype=np.float32)
        for m in range(1, N_MELS + 1):
            l, c, r = bins[m - 1], bins[m], bins[m + 1]
            if c > l:
                fb[m - 1, l:c] = (np.arange(l, c) - l) / max(c - l, 1)
            if r > c:
                fb[m - 1, c:r] = (r - np.arange(c, r)) / max(r - c, 1)
        _mel_log._fb_cache[device] = torch.from_numpy(fb).to(device)
    fb = _mel_log._fb_cache[device]
    window = torch.hann_window(N_FFT, device=x.device)
    s = torch.stft(x, n_fft=N_FFT, hop_length=HOP, win_length=N_FFT,
                   window=window, return_complex=True, center=True)
    mag = s.abs().pow(2)  # (F, T)
    mel = fb @ mag
    return torch.log1p(mel)


def _mel_l1(a, b, _unused=None):
    device = a.device
    ma = _mel_log(a, device)
    mb = _mel_log(b, device)
    return float((ma - mb).abs().mean().item())


def _stft_mag(x, n_fft, hop, win):
    window = torch.hann_window(win, device=x.device)
    s = torch.stft(
        x, n_fft=n_fft, hop_length=hop, win_length=win,
        window=window, return_complex=True, center=True,
    )
    return s.abs()


def _mrstft(a, b):
    """Same definition as score_variants._mrstft."""
    n = min(a.numel(), b.numel())
    a = a[:n]
    b = b[:n]
    total = 0.0
    for n_fft, hop, win in zip(MRSTFT_FFT_SIZES, MRSTFT_HOP_SIZES, MRSTFT_WIN_SIZES):
        ma = _stft_mag(a, n_fft, hop, win)
        mb = _stft_mag(b, n_fft, hop, win)
        sc = (mb - ma).norm(p="fro") / mb.norm(p="fro").clamp_min(1e-8)
        lm = (torch.log(ma.clamp_min(1e-7)) - torch.log(mb.clamp_min(1e-7))).abs().mean()
        total += float(sc.item()) + float(lm.item())
    return total / len(MRSTFT_FFT_SIZES)


PROMPTS = [
    ("dance", "dance"),
    ("jazz", "jazz"),
    ("ambient", "ambient"),
    ("metal", "metal"),
    ("orch", "orch"),
    ("folk", "folk"),
]


def main():
    listen_root = Path(__file__).parent / "listen"
    bf16_dir = listen_root / "bf16_60s"
    nvfp4_dir = listen_root / "nvfp4"
    device = torch.device("cuda")

    if not bf16_dir.exists() or not nvfp4_dir.exists():
        print(f"missing audio dir: bf16_dir={bf16_dir.exists()} nvfp4_dir={nvfp4_dir.exists()}")
        return 1

    print(f"{'prompt':>10s}  {'mel-L1':>8s}  {'MR-STFT':>8s}")
    print("-" * 40)
    mel_vals, mrstft_vals = [], []
    for i, (tag, _) in enumerate(PROMPTS):
        fn = f"{i+1:02d}_{tag}.wav"
        bf16_path = bf16_dir / fn
        nvfp4_path = nvfp4_dir / fn
        if not bf16_path.exists() or not nvfp4_path.exists():
            print(f"  {tag}: missing wav")
            continue
        bf16_wav = _load_wav_mono(bf16_path, MEL_SR).to(device)
        nvfp4_wav = _load_wav_mono(nvfp4_path, MEL_SR).to(device)
        n = min(bf16_wav.numel(), nvfp4_wav.numel())
        bf16_wav = bf16_wav[:n]
        nvfp4_wav = nvfp4_wav[:n]
        m_l1 = _mel_l1(nvfp4_wav, bf16_wav)
        mrstft = _mrstft(nvfp4_wav, bf16_wav)
        mel_vals.append(m_l1)
        mrstft_vals.append(mrstft)
        print(f"  {tag:>8s}  {m_l1:8.4f}  {mrstft:8.4f}")

    print("-" * 40)
    print(f"  {'mean':>8s}  {np.mean(mel_vals):8.4f}  {np.mean(mrstft_vals):8.4f}")
    print()
    print("Reference points from INDEX.md (mel-L1 / MR-STFT vs bf16):")
    print("  w8a8_skip3_cal2:    0.441 / 0.723   (slowest fp8, ~1.02x)")
    print("  w8a8_absmax_cal2:   0.499 / 0.769   (fp8 frontier, 1.52x)")
    print("  w8a8_skip20_cal2:   0.478 / 0.746   (1.40x)")
    print("  w8a8_skip5_cal2:    0.483 / 0.745   (1.15x)")


if __name__ == "__main__":
    main()
